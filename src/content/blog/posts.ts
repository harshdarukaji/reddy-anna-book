function mergePosts(): BlogPost[] {
  const seen = new Set<string>();
  const merged: BlogPost[] = [];
  for (const post of [...cricketNewsPosts, ...reddyAnnaBookPosts, ...posts]) {
    if (seen.has(post.slug)) continue;
    seen.add(post.slug);
    merged.push(post);
  }
  return merged;
}

const allPostsCache = mergePosts();

export function getAllPosts(): BlogPost[] {
  return [...allPostsCache].sort(
    (a, b) =>
      new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  );
}

export function getPostBySlug(slug: string): BlogPost | undefined {
  return allPostsCache.find((p) => p.slug === slug);
}

export function getRelatedPosts(post: BlogPost): BlogPost[] {
  if (!post.relatedSlugs?.length)
    return getAllPosts().filter((p) => p.slug !== post.slug).slice(0, 3);
  return post.relatedSlugs
    .map((s) => getPostBySlug(s))
    .filter((p): p is BlogPost => p !== undefined);
}

export function getAllCategories(): string[] {
  return [...new Set(allPostsCache.map((p) => p.category))];
}

export function getPostsByCategory(category: string): BlogPost[] {
  return getAllPosts().filter((p) => p.category === category);
}
